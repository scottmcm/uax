use crate::lookup_table::LookupTable;

use crate::properties::Script;
use Script::*;

impl From<char> for Script {
    #[inline]
    fn from(c: char) -> Self {
        if c < ROW0_LIMIT {
            return ROW0_TABLE.get_or(&(c as u8), Unknown);
        }
        if c < PLANE0_LIMIT {
            return PLANE0_TABLE.get_or(&(c as u16), Unknown);
        }
        return SUPPLEMENTARY_TABLE.get_or(&(c as u32), Unknown);
    }
}

#[test]
fn validate_tables() {
    use std::convert::TryInto;
    ROW0_TABLE.validate();
    if let Ok(x) = (ROW0_LIMIT as u32).try_into() { assert!(!ROW0_TABLE.contains(&x)); }
    PLANE0_TABLE.validate();
    if let Ok(x) = (PLANE0_LIMIT as u32).try_into() { assert!(!PLANE0_TABLE.contains(&x)); }
    SUPPLEMENTARY_TABLE.validate();
}

