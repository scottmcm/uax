use crate::lookup_table::LookupTable;

#[allow(non_camel_case_types)] // Whatever unicode says, we use
#[derive(Debug, Copy, Clone, Eq, PartialEq)]
pub enum Grapheme_Cluster_Break {
    Other,
    CR,
    Control,
    Extend,
    L,
    LF,
    LV,
    LVT,
    Prepend,
    Regional_Indicator,
    SpacingMark,
    T,
    V,
    ZWJ,
}
use Grapheme_Cluster_Break::*;

impl From<char> for Grapheme_Cluster_Break {
    #[inline]
    fn from(c: char) -> Self {
        if c < ROW0_LIMIT {
            return ROW0_TABLE.get_or(&(c as u8), Other);
        }
        if c < PLANE0_LIMIT {
            return PLANE0_TABLE.get_or(&(c as u16), Other);
        }
        return SUPPLEMENTARY_TABLE.get_or(&(c as u32), Other);
    }
}

